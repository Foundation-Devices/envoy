use rust_lib_shards::api::shard::{ShardBackup, ShardBackupFile};
use std::fs;

#[test]
fn test_shard_backup_encode_decode() {
    let backup = ShardBackup::new(
        [
            0x41, 0x42, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43,
            0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43, 0x43,
            0x43, 0x43, 0x43, 0x43,
        ],
        vec![0xDE, 0xAD, 0xBE, 0xEF],
    );

    // Test encoding
    let encoded = backup.to_bytes().expect("Failed to encode");
    assert!(!encoded.is_empty());

    // Test decoding
    let decoded = ShardBackup::from_bytes(&encoded).expect("Failed to decode");

    assert_eq!(decoded.fingerprint, backup.fingerprint);
    assert_eq!(decoded.timestamp, backup.timestamp);
    assert_eq!(decoded.shard, backup.shard);
}

#[test]
fn test_adding_shard() {
    let file_path = "serializing_test.cbor";

    let original_shard_data = vec![
        0xa2, 0x0, 0x82, 0x0, 0xa5, 0x0, 0x58, 0x20, 0xfd, 0xee, 0x76, 0xb6, 0xc9, 0xd7, 0x87,
        0xde, 0xc9, 0xb1, 0x23, 0x18, 0x2b, 0x2, 0xc5, 0x5a, 0x19, 0xab, 0x70, 0x8f, 0xab, 0x80,
        0x4a, 0xbc, 0xdd, 0xaf, 0xd5, 0xef, 0xda, 0xfb, 0x21, 0xf9, 0x1, 0x58, 0x20, 0x70, 0x12,
        0x7e, 0xb2, 0x80, 0xfc, 0x94, 0x5c, 0x80, 0xf0, 0x72, 0x7d, 0x51, 0x89, 0xbc, 0x76, 0xe8,
        0x56, 0x16, 0x4a, 0x86, 0x54, 0xf7, 0x78, 0xf3, 0xe0, 0xf7, 0x63, 0xe4, 0xdd, 0xfc, 0x86,
        0x2, 0x50, 0x99, 0x63, 0xb2, 0x13, 0x18, 0x1f, 0x7e, 0xbb, 0xab, 0x4e, 0x1f, 0x97, 0x1a,
        0x1b, 0x7b, 0xfd, 0x3, 0x2, 0x4, 0xf5, 0x1, 0x58, 0x20, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
        0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
        0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0,
    ];

    // Add the shard
    let add_result =
        ShardBackupFile::add_new_shard(original_shard_data.clone(), file_path.to_string());

    assert!(add_result.is_ok(), "Failed to add shard: {:?}", add_result);

    // Read back and verify
    let retrieved_shards = ShardBackupFile::load(file_path.to_string());

    // Should have exactly one shard
    assert_eq!(
        retrieved_shards.shards.len(),
        1,
        "Expected exactly one shard in file"
    );

    let retrieved_shard = &retrieved_shards.shards.values().next().unwrap();

    // Verify all fields match exactly
    assert_eq!(
        retrieved_shard.shard, original_shard_data,
        "Shard data mismatch"
    );
    assert!(retrieved_shard.timestamp > 0, "Timestamp should be set");

    // Cleanup
    let _ = fs::remove_file(file_path);
}
